class DeclaringArrays {  
  
  // Declaring an Array of Primitives
  int[] key1;        // Recomended
  int key2[];        // Legal, but less readable
  
  // Declaring an Array of Object References
  Thread[] threads1;  // Recomended
  Thread threads2[]; // Legal, but less readable
  
  // Multidimensional array
  String[][][] occupantName;
  String[] managerName;
  
  
  
}